package test.threadit;

import javax.swing.UIManager;
import java.awt.*;

/**
 * Title:        Threading Library
 * Description:  This is a port of the C++ Threading Library to Java. The library provides a framework for
 * an asynchronous threading model that can be easily used to build up a system using concurrency as a
 * means of abstraction.
 * Copyright:    Copyright (c) 2001
 * Company:      Ashkel Software
 * @author Ari Edinburg
 * @version 1.0
 */

public class ThreadTest {
  boolean packFrame = false;

  /**Construct the application*/
  public ThreadTest() {
    ThreadTestFrame frame = new ThreadTestFrame();
    //Validate frames that have preset sizes
    //Pack frames that have useful preferred size info, e.g. from their layout
    if (packFrame) {
      frame.pack();
    }
    else {
      frame.validate();
    }
    //Center the window
    Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
    Dimension frameSize = frame.getSize();
    if (frameSize.height > screenSize.height) {
      frameSize.height = screenSize.height;
    }
    if (frameSize.width > screenSize.width) {
      frameSize.width = screenSize.width;
    }
    frame.setLocation((screenSize.width - frameSize.width) / 2, (screenSize.height - frameSize.height) / 2);
    frame.setVisible(true);
  }
  /**Main method*/
  public static void main(String[] args) {
    try {
      UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
    }
    catch(Exception e) {
      e.printStackTrace();
    }
    new ThreadTest();
  }
}